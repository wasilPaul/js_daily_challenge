const decodeMorse = morseCode => {
  const dictionary = {
    ".-": "a",
    "-...": "b",
    "-.-.": "c",
    "-..": "d",
    ".": "e",
    "..-.": "f",
    "--.": "g",
    "....": "h",
    "..": "i",
    ".---": "j",
    "-.-": "k",
    ".-..": "l",
    "--": "m",
    "-.": "n",
    "---": "o",
    ".--.": "p",
    "--.-": "q",
    ".-.": "r",
    "...": "s",
    "-": "t",
    "..-": "u",
    "...-": "v",
    ".--": "w",
    "-..-": "x",
    "-.--": "y",
    "--..": "z",
    ".----": "1",
    "..---": "2",
    "...--": "3",
    "....-": "4",
    ".....": "5",
    "-....": "6",
    "--...": "7",
    "---..": "8",
    "----.": "9",
    "-----": "0",
    ".-.-.-": ".",
    "-.-.--": "!",
    "..--..": "?",
    "--..--": ",",
    ".----.": "'",
    "---...": ":"
  };

  return morseCode
    .split("   ")
    .map(morseWord =>
      morseWord
        .split(" ")
        .map(decodeWord => dictionary[decodeWord])
        .join("")
    )
    .join(" ");
};

const codeMorse = str => {
  const dictionary = {
    a: ".-",
    b: "-...",
    c: "-.-.",
    d: "-..",
    e: ".",
    f: "..-.",
    g: "--.",
    h: "....",
    i: "..",
    j: ".---",
    k: "-.-",
    l: ".-..",
    m: "--",
    n: "-.",
    o: "---",
    p: ".--.",
    q: "--.-",
    r: ".-.",
    s: "...",
    t: "-",
    u: "..-",
    v: "...-",
    w: ".--",
    x: "-..-",
    y: "-.--",
    z: "--..",
    1: ".----",
    2: "..---",
    "3": "...--",
    "4": "....-",
    "5": ".....",
    "6": "-....",
    "7": "--...",
    "8": "---..",
    "9": "----.",
    "0": "-----",
    ".": ".-.-.-",
    "!": "-.-.--",
    "?": "..--..",
    ",": "--..--",
    "'": ".----.",
    ":": "---..."
  };
  
  return str
    .split(" ")
    .map(word =>
      word
        .toLowerCase()
        .split("")
        .map(character => dictionary[character])
        .join(" ")
    )
    .join("   ");
};
